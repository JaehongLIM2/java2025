package ch02.lecture;

public class C06RealNumber {
    public static void main(String[] args) {
        // 실수 :
        // float : (4 byte, 16bits)
        float a = 3.14F;
        System.out.println("a = " + a);


        // double : (8 byte, 64bits)
        // 더 넓은 범위를 더 촘촘하게 표현함.
        double b = 3.14;
        System.out.println("b = " + b);

        // java 코드에서 실수 literal 은 double로 인식
        // java 에서 실수 literal 이 float 로 표현하려면 끝에 F(f)

    }
}
