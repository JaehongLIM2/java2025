package ch04.lecture;

public class C01If {
    public static void main(String[] args) {
        // if : 조건문(분기문, branch, condition)
        // () 이 true 이면 코드블럭 실행

        if (true) {
            System.out.println("true 이면 실행되는 코드");
        }

        if (false) {
            System.out.println("false 이면 실행이 안됩니다.");
        }

        // javascript 와 달리 truthy, falsy 없음


    }
}
