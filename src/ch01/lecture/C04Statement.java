package ch01.lecture;

public class C04Statement {

    // psvm 또는 main + enter : public static void main(String[] args) { }
    public static void main(String[] args) {
        // 명령문
        System.out.println("출력문1");
        // 순서대로 실행됨
        System.out.println("출력문2");
        // 명령문은 ; 으로 끝나야함
        System.out.println("출력문3");
        System.out.println("출력문4");
        System.out.println("출력문5");
        // ctrl + shift + enter : 나머지 문장을 자동완성
        System.out.println("출력문6");

        // ctrl + alt + l : 들여쓰기 맞추기(code formatting)

        // ctrl + d : 현재 커서의 한 줄 아래에 복사
        System.out.println("출력문");
        System.out.println("출력문");
        System.out.println("출력문");

        // alt + shift + 화살표 위아래 : 코드 위아래로 이동

        // ctrl + alt + enter : 윗 줄에 새로 코드 입력
        // shift + enter : 아랫 줄에 새로 코드 입력


    }
}
