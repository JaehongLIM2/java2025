package ch06.exercise.p16;

public class Printer {
    void println(int values) {
        System.out.println(values);
    }

    void println(boolean values) {
        System.out.println(values);
    }

    void println(double values) {
        System.out.println(values);
    }

    void println(String values) {
        System.out.println(values);
    }
}
