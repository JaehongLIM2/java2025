package ch06.exercise.p17;

public class Printer {
    static void println(int values) {
        System.out.println(values);
    }

    static void println(boolean values) {
        System.out.println(values);
    }

    static void println(double values) {
        System.out.println(values);
    }

    static void println(String values) {
        System.out.println(values);
    }
}
